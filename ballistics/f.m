%-------------------------------------------------------------------------------
%
%   Баллистическая задача без учета сопротивления среды
%   система ОДУ движения
%
%-------------------------------------------------------------------------------
function dYdt = f(Y, t)

  g = 9.81; % Ускорение свободного падения

  dYdt(1) = Y(3); % dx/dt = vx
  dYdt(2) = Y(4); % dy/dt = vy
  dYdt(3) = 0;    % dvx/dt = 0
  dYdt(4) = -g;   % dvy/dt = -g
  
endfunction

